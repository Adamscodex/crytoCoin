const counter = document.querySelector('.sale');
let count = 0;

function animateCount() {
  counter.textContent = count.toString().padStart(3, '0');
  count++;
  if (count <= 100) { // adjust the limit as needed
    requestAnimationFrame(animateCount);
  }
}

animateCount();



